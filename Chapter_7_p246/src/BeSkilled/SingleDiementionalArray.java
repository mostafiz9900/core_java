package BeSkilled;

public class SingleDiementionalArray {

    public static void main(String[] args) {
        int p[] = {50, 36, 48, 26, 55, 88};

        int q[] = {50, 36, 26, 55, 88};
        System.out.println();
        for (int i : p) {
            System.out.print(i + "  ");

        }
        System.out.println();
        for (int i : q) {
            System.out.print(i + "  ");
        }

        System.out.println();
    }
}
